int32_t main(){
    int n;
    cin>>n;
    int a[n+1];
    a[0]=-1;
    for(int i=1;i<=n;i++)cin>>a[i];
    sort(a,a+n+1);
    int dp[n+1][n+1];
    for(int i=0;i<=n;i++){
        for(int j=0;j<=n;j++){
            dp[i][j]=0;
        }
    }
    int count=1;
    while(count!=(n)){
        int i=1,j=count+1;
        while(i<=(n-count)){
            dp[i][j]=min(dp[i][j-1],dp[i+1][j])+(a[j]-a[i]);
            i++;
            j++;
        }
        count++;
    }
    cout<<dp[1][n]<<"\n";
}